package seminars;

public class Person {
    public String familyName;
    public String name;
    public String fatherName;
    public String date;
    public String phone;
    public String sex;

    Person(String familyName, String name, String fatherName, String date, String phone, String sex) {
        this.familyName = familyName;
        this.name = name;
        this.fatherName = fatherName;
        this.date = date;
        this.phone = phone;
        this.sex = sex;
    }
}
